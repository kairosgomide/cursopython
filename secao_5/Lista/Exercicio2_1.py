numero1 = int(input('Escreva um número aleatório'))
numero2 = int(input('Escreva um outro número aleatório'))

print(f'a soma entre esses dois número é {numero1 + numero2}')
print(f'a subtração entre esses dois número é {numero1 - numero2}')
print(f'a multiplicação entre esses dois número é {numero1 * numero2}')
print(f'a divisão entre esses dois número é {numero1 / numero2}')