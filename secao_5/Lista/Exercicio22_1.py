altura = int(input('Digite o valor da altura de uma caixa retangular: '))
largura = int(input('Digite o valor da largura de uma caixa retangular: '))
comprimento = int(input('Digite o valor do comprimento de uma caixa retangular: '))

volume = altura * largura * comprimento

print(f'O volume da caixa retangular é {volume} m³')