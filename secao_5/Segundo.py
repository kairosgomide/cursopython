#5.26 Tipos Básicos

print(True) #tipo booleano
print(False) #tipo booleano

print(1.2 + 1) #tipo real ou float
print(1 + 1) #tipo inteiro

print('Olá Mundo') #tipo string
print('Você é ' + 3 * 'muito ' + 'legal!')
print('Kairos ' + 5 * '15 ' + 'anos')
#print(3 + '3') -> ambiguidade
print([1, 2, 3]) #lista
print({'nome': 'Kairos', 'idade': 15}) #dicionário
print(None) #Algo sem valor