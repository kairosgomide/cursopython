numero = int(input('Digite um número entre 1 a 7: '))

if numero == 1:
    print(f'DOMINGO')
elif numero == 2:
    print(f'SEGUNDA-FEIRA')
elif numero == 3:
    print(f'TERÇA-FEIRA')
elif numero == 4:
    print(f'QUARTA-FEIRA')
elif numero == 5:
    print(f'QUINTA-FEIRA')
elif numero == 6:
    print(f'SEXTA-FEIRA')
elif numero == 7:
    print(f'SÁBADO')
else:
    print(f'Número Invalido')